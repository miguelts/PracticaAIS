    private void jMenuItemExportarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItemExportarActionPerformed
        contactos = null;
        FileNameExtensionFilter filter = new FileNameExtensionFilter("Archivos CSV", "csv");
        ImageIcon icon = new ImageIcon(getClass().getResource("/agendaTelefonica/CSV.png"));
        JFileChooser chooser = new JFileChooser();
        chooser.setApproveButtonText("Guardar");
        chooser.setApproveButtonToolTipText("Guarda el archivo seleccionado");
        chooser.setDialogTitle("Exportar");
        chooser.setFileFilter(filter);
        chooser.setSelectedFile(new File(".csv"));
        chooser.setToolTipText("Guarde un " + "\"" + "Archivo.csv" + "\"");
        if(chooser.showOpenDialog(this) == JFileChooser.APPROVE_OPTION) {
            String nombre;
            String ruta;
            if(chooser.getSelectedFile().getAbsolutePath().endsWith(".csv")) {
                nombre = chooser.getSelectedFile().getName();
                ruta = chooser.getSelectedFile().getPath();
            }
            else {
                nombre = chooser.getSelectedFile().getName() + ".csv";
                ruta = chooser.getSelectedFile().getPath() + ".csv";
            }
            try (FileOutputStream f = new FileOutputStream(ruta)) {
                ObjectOutputStream so= new ObjectOutputStream(f);
                so.writeObject(contactos);
                f.close();
                JOptionPane.showMessageDialog(this, "Exportado con éxito de " + nombre, "Exportar Agenda", JOptionPane.INFORMATION_MESSAGE, icon);
            } catch (IOException ex) {
                JOptionPane.showMessageDialog(this, "Error al exportar al fichero", "Exportar Agenda", JOptionPane.ERROR_MESSAGE, icon);
            }
        }
    }//GEN-LAST:event_jMenuItemExportarActionPerformed

    private void jMenuItemImportarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItemImportarActionPerformed
        contactos = null;
        FileNameExtensionFilter filter = new FileNameExtensionFilter("Archivos CSV", "csv");
        ImageIcon icon = new ImageIcon(getClass().getResource("/agendaTelefonica/CSV.png"));
        JFileChooser chooser = new JFileChooser();
        chooser.setDialogTitle("Importar");
        chooser.setFileFilter(filter);
        chooser.setSelectedFile(new File(".csv"));
        chooser.setToolTipText("Abra un " + "\"" + "Archivo.csv" + "\"");
        if(chooser.showOpenDialog(this) == JFileChooser.APPROVE_OPTION) {
            String nombre = chooser.getSelectedFile().getName();
            if(nombre.endsWith(".csv")) {
                String ruta = chooser.getSelectedFile().getPath();
                try { FileInputStream in = new FileInputStream(ruta);
                    ObjectInputStream ob = new ObjectInputStream(in);
                    contactos = (ArrayList<Contacto>) ob.readObject();
                    JOptionPane.showMessageDialog(this, "Importado con éxito de " + nombre, "Importar Agenda", JOptionPane.INFORMATION_MESSAGE, icon);
                }
                catch (IOException | ClassNotFoundException ex) {
                    JOptionPane.showMessageDialog(this, "Error al importar del fichero", "Importar Agenda", JOptionPane.ERROR_MESSAGE, icon);
                }
            }
            else {
                JOptionPane.showMessageDialog(this, "Solo se pueden leer archivos .csv", "Importar Agenda", JOptionPane.WARNING_MESSAGE, icon);
            }
        }
    }//GEN-LAST:event_jMenuItemImportarActionPerformed

